package com.dch.viewbean;

public class Grobe {
	private int total;
	private int page;
	private int curr;
	private int cify;
	private String desc;
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getCurr() {
		return curr;
	}
	public void setCurr(int curr) {
		this.curr = curr;
	}
	public int getCify() {
		return cify;
	}
	public void setCify(int cify) {
		this.cify = cify;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
}
